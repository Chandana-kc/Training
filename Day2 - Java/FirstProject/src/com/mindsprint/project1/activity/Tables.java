package com.mindsprint.project1.activity;

public class Tables {
    public static void main(String[] args) {
        int num=10;
        System.out.println("Multiplication table of " +num+ ":");
        for(int i=1;i<=10;i++){
            System.out.println(num + " X " + i + "=" + (num * i ));

        }
    }
}
